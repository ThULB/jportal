package org.mycore.backend.hibernate.tables;

import java.io.Serializable;

public class MCRXMLTABLEPK implements Serializable {

    private static final long serialVersionUID = 8330273148129972169L;

    private String id;

    private int version;

    public MCRXMLTABLEPK(String id, int version) {
        this.id = id;
        this.version = version;
    }

    public MCRXMLTABLEPK() {
    }

    public String getId() {
        return id;
    }

    public int getVersion() {
        return version;
    }


    public void setId(String id) {
        this.id = id;
    }

    public void setVersion(int version) {
        this.version = version;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#hashCode()
     */
    @Override
    public int hashCode() {
        final int PRIME = 31;
        int result = 1;
        result = PRIME * result + ((id == null) ? 0 : id.hashCode());
        result = PRIME * result + version;
        return result;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#equals(java.lang.Object)
     */
    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        final MCRXMLTABLEPK other = (MCRXMLTABLEPK) obj;
        if (id == null) {
            if (other.id != null)
                return false;
        } else if (!id.equals(other.id))
            return false;
        if (version != other.version)
            return false;
        return true;
    }

}
